package ArraysPack;

public class ArrayClass5 {

	public static void main(String[] args) {
		
		int [] marks = new int[11];
		
		marks[5] = -789;
		
		marks[0] = 453;
		
		marks[8] = 86546;
		
		marks[3] = 3251;

		marks[10] = -100;
	
		for(int y=0;y<11;y++) {
			System.out.println(marks[y]);
		}
		
	}

}
