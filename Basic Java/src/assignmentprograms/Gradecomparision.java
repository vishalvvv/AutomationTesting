package assignmentprograms;

public class Gradecomparision {

	public static void main(String[] args) {
		
		int grade = 70;
		
		if(grade>=80) {
			System.out.println("A");
		}
		else if(grade>=70) {
			System.out.println("B");
		}
		else if(grade>=60) {
			System.out.println("C");
		}
		else {
			System.out.println("failed");
		}

	}

}
